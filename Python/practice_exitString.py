## 탈출 문자

print("백문이 불여일견 백견이 불여일타")
# 두 줄에 걸쳐서 출력
print("백문이 불여일견\n백견이 불여일타")   # \n : 줄바꿈

print('저는 "ㅁㅁㅁ"입니다.')
print("저는 \"ㅁㅁㅁ\"입닌다.")     # \" \' : 문장 내에서 따옴표처럼 사용 가능

print("C:\\Users\\sunge\\OneDrive\\Desktop\\PythonWorkspace")     # \\ : 문장 내에서 \

print("Red Apple\rPine")     # \r : 커서를 맨 앞으로 이동 (즉, "Red "가 Pine으로 덮어쓰기 된 것이다.)

print("Redd\bApple")     # \b : 백스페이스 (한 글자 삭제)

print("Red\tApple")     # \t : 탭